# capitalize method
one = "BANGLADESH"
print(one.capitalize())
print('--------------------')

# casefold method
two = "What Is Your Name?"
print(two.casefold())
print('--------------------')

# center method
three = "Maa"
print(three.center(5, "I"))
print('--------------------')

# upper method
four = "hello world."
print(four.upper())
print('--------------------')

# lower method
five = "WHAT"
print(five.lower())
print('--------------------')

# format method
six = "I am {}."
print(six.format("Sady"))
print('--------------------')


