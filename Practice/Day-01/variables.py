# Variable Declaration
first_number = 10
# Output
print(first_number) 
print('--------------------')

# Variable names are case sensitive. For example: one and One are not same.
country_name = 'Bangladesh'
Country_name = 'Pakistan'
print(country_name)
print(Country_name)
print('--------------------')

# Many value to Multiple variable
a, b, c = 10, 20, 30
print(f'a = {a}, b = {b}, c = {c}')
print('--------------------')

# One value to Multiple Variables
x = y = z = 100
print(f'x = {x}, y = {y}, z = {z}')
print('--------------------')

# Unpack Collection
country_list = ['bangladesh', 'india', 'pakistan']
first_country, second_country, third_country = country_list
print(f'First-Country = {first_country}, Second-Country = {second_country} and Third-Country = {third_country}')


