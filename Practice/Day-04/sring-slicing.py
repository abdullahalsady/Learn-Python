name = "abdullah"
# it will start from index two and end before index four.
print(name[2:4]) 
print('--------------------')

# it will start from index zero and end before index five.
print(name[:5])
print('--------------------')

# it will start from index and end after last index.
print(name[:])
print('--------------------')

# negative slicing [negative slicing means it will start counting from the index and end counting also from the last end index. rememver this think negative indexing start from 1.]
print(name[-5: -2])