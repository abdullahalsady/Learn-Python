# upper case
my_name = "abdullah"
print(my_name.upper())
print("---------------")

# lower case
fathers_name = "Shafiqul"
print(fathers_name.lower())
print("---------------")

# remove white space
mothers_name = "   shilpi   "
# this is the way to use two methods in a time.
print(mothers_name.strip().upper())
print("---------------")

# replace
greeting = "How are you?"
print(greeting.replace("How are", "I'm fine and"))
print("---------------")

# split string
love = "I love you!"
print(love.split(' '))
print("---------------")
